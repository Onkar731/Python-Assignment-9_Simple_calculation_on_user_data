"""
Write a python script to calculate area of a rectangle
"""

# taking length and breadth of a rectangle from the user
length, breadth = int(input("Enter length and breadth of the rectangle : ")), int(input())

# calculating area of rectangle
area = length * breadth

# printing area of the rectangle
print("Area of the rectangle is", area)

